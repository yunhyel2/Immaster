<p class="sub_title">제10조 (이용계약의 종료)</p>
<p class="content">① 회원은 다음 각호에서 정한 바에 따라 이용계약을 해지할 수 있습니다. </p> 
<ul class="content">
    <li>1. 회원은 언제든지 회사에 해지의 의사를 통지하여 이용계약을 해지할 수 있습니다. 단, 해지의 사를 통지하기 전에 현재 진행 중인 모든 상품의 거래를 완료하거나 철회 또는 취소하여야 하며, 거래의 철회 또는 취소로 인한 불이익은 회원 본인이 부담하여야 합니다. 회원이 이 약관과 관계 법령 등을 위반하여 이용이 정지된 경우 회사가 재발방지를 위하여 회원의 이용계약 해지를 제한할 수 있습니다. 이용계약은 회사에 회원의 해지의사가 도달한 시점에 종료됩니다.</li>
    <li>2. 회사가 해지할 때에는 회사는 회원에게 이메일(E-mail), 전화, 팩스, 기타의 방법으로 해지 사유 를 밝혀 해지 의사를 통지합니다. 거래의 안전을 위하여 회사는 동일인 소유로 확인되는 회원 아이디(ID)에 대하여 일괄 해지 의사를 통지할 수 있습니다. 이용계약은 회사의 해지의사를 회원에 게 통지한 시점에 종료됩니다.</li>
    <li>3. 회원의 의사로 해지한 후 회원 재가입, 임의 해지 등을 반복적으로 행하여 회사가 제공하는 각 종 할인쿠폰, 이벤트 혜택 등 경제적 이익을 편법으로 수취하거나 이 과정에서 명의를 무단으로 사용하는 등 편법과 불법행위를 하는 것을 차단하고자 회사는 회원에게 회원 탈퇴한 날로부터 7 일이 지나야 재가입 할 수 있게 하고 있으며 회원은 이 약관이 정하는 회원가입절차와 관련 조항 에 따라 회사에 통지된 재이용 의사를 회사로부터 승낙 받은 이후에 서비스를 재이용할 수 있습니다.</li>
    <li>4. 회원 재가입, 임의해지 등을 반복적으로 행하여 회사가 제공하는 각종 할인쿠폰, 이벤트 혜택 등의 경제적 이익을 편법으로 수취하거나 이 과정에서 명의를 무단으로 사용하는 등 편법과 불법 행위를 하는 것을 차단하고자 회원탈퇴 후 1개월 동안 해당 회원의 아이디, 성명, CI, DI, 탈퇴일자, 탈퇴사유 등 개인정보를 보관합니다.</li>
</ul>
<p class="content">② 회사는 다음 각호에서 정한 바에 따라 이용계약을 해지할 수 있습니다.</p> 
<ul class="content">
    <li>1. 회사는 회원에게 다음 각목의 1에 해당하는 사유가 발생하거나 확인되면 이용계약을 해지할 수 있습니다. 
        <ul>
            <li>가. 다른 회원·회사(직원·상담원 포함)의 권리나 명예, 신용 기타 정당한 이익을 침해하거나 대한민 국 법령 또는 공서양속에 위배되는 행위를 한 경우</li>
            <li>나. 서비스의 원활한 진행을 방해하거나 방해할 우려가 있는 아래의 행위 등을 하거나 시도한 경우
                <ul>
                    <li>- 합리적 사유 없이 상습적·악의적으로 이의를 제기하는 행위</li>
                    <li>- 구매한 상품 또는 서비스에 특별한 하자가 없음에도, 상습적으로 사용 후 취소, 환불 요청 등을 하는 행위</li>
                    <li>- 그 외 회사가 정한 안전거래 이용규칙을 위반한 경우</li>
                </ul>
            </li>
            <li>다. 회원의 누적 신용도가 일정 점수 이하에 이른 경우</li>
            <li>라. 고의로 회사의 영업을 방해 하거나 판매부적합 상품을 등록할 경우</li>
            <li>마. 회원이 이 약관에 위배되는 행위를 하거나 이 약관에서 정한 해지사유가 발생한 경우 (마스터 약관의 적용을 받는 자가 해당 약관을 위반한 경우 포함)</li>
            <li>바. 회원에게 제9조에서 정한 이용계약의 승낙거부사유가 있음이 확인된 경우</li>
            <li>사. 기타 회사가 합리적인 판단에 따라 서비스의 제공을 거부할 필요가 있다고 인정할 경우</li>
        </ul>
    </li>
    <li>2. 회사가 해지할 때에는 회사는 회원에게 이메일(E-mail), 전화, 팩스, 기타의 방법으로 해지 사유 를 밝혀 해지 의사를 통지합니다. 이용계약은 회사의 해지의사를 회원에게 통지한 시점에 종료됩니다.</li>
    <li>3. 본 항에 따라 회사가 이용계약을 해지하더라도, 해지 이전에 이미 체결된 매매계약의 완결에 관해서는 이 약관이 계속 적용됩니다.</li>
    <li>4. 본 항에서 정한 바에 따라 이용계약이 종료될 때에는 회사는 별도 통지 없이 해당 회원과 관련된 광고와 거래를 취소할 수 있고, 일반회원이 상품구매대금을 신용카드로 결제했을 때에는 해 당 신용카드매출을 취소할 수 있으며, 회원에게 부가적으로 제공한 각종 혜택을 회수할 수 있습니다.</li>
    <li>5. 본 항에서 정한 바에 따라 이용계약이 종료되면 회사는 회원의 재이용 신청을 그 승낙하지 않 을 수 있습니다.</li>
</ul>
<p class="content">③ 이용계약의 종료와 관련하여 발생한 손해에 대해서는 이용계약이 종료된 해당 회원이 책임을 져야 하고, 회사는 일절 책임을 지지 않습니다.</p>
<p class="content">④ 이용계약이 해지된 경우 회원 본인 계정에 등록된 게시물 일체는 삭제됩니다만 다른 회원에 의해 스크랩되어 게시되거나 공용 게시판에 등록된 게시물은 삭제되지 않습니다.</p>

<p class="sub_title">제11조 (마스터회원등급 및 평가제도)</p>
<ul class="content">
    <li>① 회사는 서비스를 이용하여 상품을 거래했을 때 부과되는 점수를 기준으로 하여 마스터회원레벨 및 레벨에 따른 일정 혜택 등을 회원에게 부여할 수 있습니다. 마스터 회원레벨 및 그에 따른 혜택 등은 서비스 이용실적, 기타 회사가 정하는 이용정책에서 정한 기준에 따라 부여되며, 회원 등급 정책 및 그에 따른 혜택 등에 대한 변경이 있으면 해당 서비스 화면에 게재하거나 회사가 운영하는 인터넷 사이트 초기화면에 변경 공지합니다).</li>
    <li>② 해당 마스터회원이 자신에게 부여된 레벨 구성요소에 대하여 이의를 제기하면 회사는 소명내용, 해당 회원의 신용도 등 제반 상황을 고려하여 레벨 구성요소의 전부 또는 일부를 조정할 수 있습니다.</li>
    <li>③ 마스터회원등급을 부여하는 경우에도 회원의 레벨은 상품의 거래가 이뤄지는 경우 참고자료로 활용될 뿐이며, 회원의 신용을 보증하거나 금융상의 신용상태를 의미하는 것은 아닙니다.</li>
    <li>④ 회사는 상품평가제도를 시행할 수 있으며, 상품평가제도는 상품에 대한 구매확정이 되는 경우 이를 이용한 일반회원이 해당 거래에 대한 만족도를 회사가 정한 단계별로 표시하는 형태로 시행되며, 표시된 만족도는 해당 상품 관련 마스터에 대한 평가요소로 반영됩니다. 거래만족도 항목 및 단계는 해당 서비스 화면에 표시합니다. </li>
    <li>⑤ 상품평가는 구매확정이 되는 경우에만 작성할 수 있으며, 공개를 원칙으로 합니다. 리뷰를 작성한 회원은 자신이 작성한 리뷰를 삭제할 수 있습니다. 다만, 다수 일반회원 편의를 위해 공개된 상품평의 삭제에 대한 제한이 있을 수 있습니다.</li>
    <li>⑥ 회사는 리뷰 및 관련 내용이 욕설, 비방, 명예훼손 등의 내용이거나 상품 평가와 무관한 내용을 담고 있어 공개가 부적절하다고 판단되면 해당 리뷰와 첨부된 의견을 삭제할 수 있습니다. 상품평이 삭제될 때에는 리뷰에 첨부된 의견도 함께 삭제될 수 있습니다. 이와 관련한 자세한 설명은 해당 서비스 화면에 게재합니다.</li>
    <li>⑦ 거래 상대방에 대한 거래만족도 평가와 리뷰 작성은 전적으로 평가 당사자의 책임 하에 이루어지며, 회사는 내용의 사실 여부에 개입하지 않습니다.</li>
    <li>⑧ 회원이 회원등급 및 회원평가제도의 목적과 취지에 어긋나는 행위를 하면 회사는 해당 평가결과를 삭제하고 관련 회원에 대한 서비스 이용자격을 정지하는 등 제재를 가할 수 있습니다.</li>
    <li>⑨ 아래와 같은 내용에 해당하면 리뷰를 삭제하거나 회원의 이용자격이 정지될 수 있습니다.
        <ul>
            <li>1. 회원등급 또는 거래만족도를 높이려는 목적으로 만족도를 평가하는 행위</li>
            <li>2. 높은 만족도를 주고 리뷰를 쓰는 대가로 금전 또는 기타의 보상을 받거나 받기로 약정하고 이루어진 평가결과 및 그 평가행위</li>
            <li>3. 리뷰를 통하여 타인을 모욕하거나 타인의 명예와 신용을 훼손하는 등의 행위 및 그 평가결과</li>
            <li>4. 기타 리뷰를 통한 평가제도의 목적과 취지에 반하여 평가제도를 이용하는 행위</li>
        </ul>
    </li>
</ul>
 
<p class="sub_title">제12조 (회원관리)</p>
<ul class="content">
    <li>① 회사는 이 약관과 관계 법령 및 상거래의 일반원칙을 위반한 회원에게 다음과 같은 조치를 할 수 있습니다.
        <ul>
            <li>1. 신용점수 차감</li>
            <li>2. 회사가 부가적으로 제공한 혜택의 일부 또는 전부의 회수</li>
            <li>3. 특정 서비스 이용제한</li>
            <li>4. 이용계약의 해지</li>
            <li>5. 손해배상의 청구</li>
        </ul>
    </li>
