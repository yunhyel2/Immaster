            <li>6. 회사로부터 회원자격정지 조치 등을 받은 회원이 그 조치 기간에 이용계약을 임의 해지하고 재이용신청을 하는 경우(회원에게 재이용이 허락되더라도, 서비스 이용과정에서 본인확인 후 회원 자격정지 조치를 받았던 사실이 확인되면 해당 ID의 이용계약이 해지될 수 있음)</li>
            <li>7. 기타 이 약관에 위배되거나 위법 또는 부당한 이용신청임이 확인된 경우</li>
