<p class="sub_title">제1장 총칙</p>
<p class="sub_title">제1조 (목적)</p>
<p class="content">이 약관은 “투어텔레콤”(이하 "회사")가 "아임마스터(I’M MASTER)”서비스를 제공하고 회원이 이를 이용함에 있어 상품 또는 용역(이하 통칭하여 “상품”)을 거래하는 자 간의 권리, 의무 등 기타 필요사항, 회원과 회사간의 권리, 의무, 책임사항 및 회원의 서비스 이용절차 등에 관한 사항을 규정함을 목적으로 합니다.</p>

<p class="sub_title">제2조 (용어의 정의)</p>
<p class="content">① 이 약관에서 사용하는 용어의 정의는 다음과 같습니다.</p>
<ul class="content">
    <li>1. 아임마스터 서비스(이하 “서비스”): 회사가 Mobile App. 및 Web 형태로 제공하는 아임마스터 및 아임마스터 관련 제반 서비스를 의미합니다.</li>
    <li>2. 레슨: 마스터회원이 개설하는 클래스, 수업 형식의 활동으로 일반회원이 이용할 수 있는 서비스를 의미합니다.</li>
    <li>3. PLAY: 마스터회원이 개설하는 모임, 스터디, 놀이, 봉사 형식의 활동으로 일반회원이 이용할 수 있는 서비스를 의미합니다.</li>
    <li>4. 회원: 회사가 정한 가입절차에 따라 회원등록을 하고 회사가 제공하는 서비스를 이용하는 자를 말합니다. 회원은 다음과 같이 일반회원과 마스터회원으로 구분됩니다.
        <ul>
            <li>가. 일반회원: 아임마스터의 이용약관에 동의하고 아임마스터 내에서 제공되는 서비스를 이용하며, 레슨, PLAY 등의 상품구매 및 구매와 관련하여 제공되는 서비스를 이용할 수 있는 회원을 말합니다.</li>
            <li>나. 마스터회원(이하 “마스터”): 일반회원 중에서 마스터회원으로 지원하여 회사가 정한 일정 기준을 충족해 마스터로 승인된 회원을 말합니다.</li>
        </ul>
    </li>
    <li>5. 견적문의: 일반회원이 참가하고 싶은 레슨 또는 PLAY에 대한 구체적인 정보를 담고 있는 요청 서로 해당하는 정보에 부합하는 마스터에게로 전달됩니다.</li>
    <li>6. 다이렉트신청: 일반회원이 특정 마스터에게 원하는 레슨 또는 PLAY에 대한 구체적인 정보를 담아 전달하는 요청서입니다.</li>
    <li>7. 아이디(ID): 회원의 식별과 회원의 서비스 이용을 위하여 회원이 설정하고 회사가 승인하여 회사에 등록된 영문과 숫자 조합을 말합니다.</li>
    <li>8. 비밀번호(Password): 회원확인 및 회원의 비밀보호를 위하여 회원이 스스로가 설정해 회사에 등록한 영문과 숫자 등의 조합을 말합니다.</li>
    <li>9. 마스터레벨: 회사는 회사가 제공한 서비스를 이용한 결과 등 일정한 기준에 따라 마스터회원의 등급을 구분하여 운영할 수 있으며, 이를 마스터레벨이라 표현합니다.</li>
    <li>10. 리뷰: 상품 구매 후 해당 상품에 대한 종합적인 평가를 기록한 글, 이미지, 영상물 등의 게재물을 말합니다.</li>
    <li>11. 본인확인: 휴대전화 인증(또는 i-PIN 인증)을 통하여 회원이 본인인지 여부를 확인하거나 사업자 회원의 상호와 사업자 등록번호의 일치 여부 및 기재항목을 확인하는 것을 의미합니다.</li>
    <li>12. 영업일: 회사가 정상적으로 서비스를 제공한 날로서 토요일, 일요일 및 법정 공휴일을 제외한 날을 말합니다.</li>
    <li>13. 결제대금 보호서비스: 회원의 구매 결제가 서비스 내에서 이루어지는 경우 이 결제를 보호하기 위하여 전자상거래 등에서의 소비자보호에 관한 법률 제13조 제2항 10호에 따라 일정기간 결제대금을 예치하거나 소비자피해보상보험계약등을 체결하는 서비스를 말합니다.</li>
</ul>
<p class="content">② 제1항에서 정의되지 않은 용어의 의미는 일반적인 거래관행을 따릅니다.</p>
<p class="sub_title">제3조 (효력 우선순위)</p>
<p class="content">제1장 및 제3장의 규정과 개별서비스인 제2장의 규정이 상충할 때에는 개별서비스 관련 규정(제2 장)이 전체서비스 관련 규정(제1장 및 제3장)보다 우선적인 효력을 갖습니다.</p>

<p class="sub_title">제4조 (약관의 명시, 효력 및 변경)</p>
<ul class="content">
    <li>① 회사는 정의된 약관을 이용자가 알 수 있도록 회사의 서비스가 제공되는 서비스 웹사이트의 첫 화면에 게시합니다.</li>
    <li>② 회사는 합리적인 사유가 발생하면 약관의 규제에 관한 법률, 전자거래기본법, 전자서명법, 정 보통신망 이용촉진 및 정보보호 등에 관한 법률, 전자상거래 등에서의 소비자보호에 관한 법률 등 관련 법령에 위배되지 않는 범위 안에서 이 약관을 변경 할 수 있으며, 회사가 약관을 변경할 때에는 적용일자와 변경사유를 밝혀 그 적용일자 7일 전부터 서비스 사이트에 공지합니다. 다만, 이용자에게 불리한 약관을 변경할 때에는 해당 내용을 그 적용일자 30일 전부터 공지하며, 이메 일(e-mail), 문자메시지(SMS) 등으로 이용자에게 개별 통지합니다(이용자의 연락처 미기재, 변경 등으로 개별 통지가 어려울 때에는 본 약관에 따라 공지를 하는 것을 개별 통지한 것으로 간주합 니다).</li>
    <li>③ 이 약관에 동의하는 것은 정기적으로 회사가 운영하는 인터넷 사이트를 방문하여 약관의 변경 사항을 확인하는 것에 동의함을 의미합니다. 변경된 약관에 대한 정보를 알지 못하여 발생하는 회원 또는 이용자의 피해에 대해 회사는 책임을 지지 않습니다.</li>
    <li>④ 변경된 약관에 동의하지 않는 회원은 회원 탈퇴(이용계약의 해지)를 요청할 수 있으며, 회사가 제2항에 따라 이용자에게 약관 변경 적용일까지 거부의사를 표시하지 않으면, 약관의 변경에 동의한 것으로 간주한다는 내용을 공지 또는 통지하였음에도 이용자가 명시적으로 약관 변경에 대한 거부의사를 표시하지 않으면, 회사는 이용자가 변경된 약관에 동의한 것으로 간주합니다.</li>
</ul>
<p class="sub_title">제5조 (약관 외 준칙)</p>
<ul class="content">
    <li>① 이 약관에 밝히지 않은 사항은 전자상거래 등에서의 소비자보호에 관한 법률 등 관련 법령의 규정과 일반 상관례를 따릅니다.</li>
    <li>② 회사는 서비스 내의 특정한 기능에만 적용될 사항(이하 “개별약관”)을 별도로 정하여 이를 서비스 App. 등을 통하여 공지할 수 있습니다.</li>
    <li>③ 회사는 서비스의 이용에 관한 세부적인 사항(이하 "이용정책")을 정하는 경우 이를 서비스 App. 등을 통하여 별도로 공지할 수 있습니다.</li>
    <li>④ 개별약관이나 이용정책의 변경이 있으면 회사는 그 변경 내용의 개별약관 및 이용정책을 게시한 서비스App. 내에 제4조 제2항에서 정한 기간에 따라 해당 변경 내용을 공지합니다.</li>
</ul>
<p class="sub_title">제6조 (이용계약의 성립)</p> 
<ul class="content">
    <li>① 회사가 제공하는 서비스에 관한 이용계약(이하 "이용계약")은 서비스를 이용하고자 하는 자의 이용신청을 회사가 승낙함으로써 성립하며, 이때 서비스를 이용하고자 하는 자는 회사가 온라인 으로 제공하는 회원가입 신청 양식에 따라 필요한 각각의 항목에 해당 내용을 기재한 후 이 약관에 동의한다는 의사 표시를 해야 합니다.</li>
    <li>② 회원가입의 성립시기는 회사의 이용승낙 의사를 해당 서비스화면에 게시하거나, 이메일(Email) 또는 기타 회사가 정하는 방법으로 이용신청자에게 도달한 시점으로 합니다.</li>
    <li>③ 회원가입은 개인의 경우 만 14세 이상이면 신청할 수 있으며, 사업자(개인사업자 및 법인사업자 포함)는 가입 신청일 현재 정상적인 영업활동이 가능해야 신청할 수 있습니다.</li>
    <li>④ 마스터회원이 등록 신청을 할 때에는 본인확인기관을 통하여 휴대전화 인증(또는 i-PIN 인증) 으로 본인여부를 확인하는 방식의 본인확인절차를 진행하며(개인마스터의 경우 본인확인 이외에 회사가 정하는 별도의 수단으로 추가 인증을 요구할 수 있음), 사업자 회원은 상호와 사업자 등록 번호의 일치 여부를 확인하고 제 8조에 정한 기재항목을 확인하는 방식으로 본인확인절차를 진행 합니다. 또한 상기의 방식으로 본인확인절차를 거칠 수 없는 이용신청이 있는 때에는 해당 신청 자에게 증빙자료를 요청할 수 있습니다.</li>
    <li>⑤ 탈퇴 후 회원 재가입, 임의 해지 등을 반복하거나 다수의 아이디(ID)로 가입하여 회사가 제공 하는 각종 할인쿠폰, 이벤트 혜택 등으로 경제적 이익을 편법으로 수취하고, 이 과정에서 명의를 무단으로 사용하는 등 편법과 불법 행위를 차단하고자 회사는 다수의 아이디(ID)보유 회원에 대하여 최초 본인 확인된 아이디(ID)에 한하여 일부 서비스 이용을 허용할 수 있으며, 회원이 탈퇴한 후 7일이 지나지 않은 회원의 서비스 가입신청을 거부할 수 있습니다.</li>
</ul>
<p class="sub_title">제7조 (개인정보의 변경, 보호)</p>
<ul class="content"> 
    <li>① 회원은 이용신청 시 기재한 사항이 변경되었을 경우에는 상당한 기간 이내에 회원정보 수정 등의 방법으로 그 변경사항을 수정하여야 합니다. 단, 이름, 아이디(ID) 등은 변경할 수 없습니다.</li>
    <li>② 수정하지 않은 정보로 말미암아 발생하는 손해는 해당 회원이 부담하며, 회사는 이에 대해 아무런 책임을 지지 않습니다.</li>
    <li>③ 회사는 관련법령이 정하는 바에 따라서 이용자의 개인정보를 보호하기 위하여 노력하며, 이용 자의 개인정보를 보호하기 위한 정책을 수립하고 개인정보보호 책임자를 지정하여 이를 게시합니다.</li>
    <li>④ 이용자의 개인정보보호는 관련 법령 및 회사가 정하는 개인정보처리방침에서 정한 바를 따릅니다.</li>
</ul>
<p class="sub_title">제8조 (서비스 이용 신청)</p>
<ul class="content"> 
    <li>① 회사가 제공하는 서비스에 가입 하여 이용하고자 하는 자는 회사에서 제공한 회원가입 신청양 식에 따라 필요사항을 기재하여야 합니다. 회원가입 양식은 필수 기재 항목과 선택 기재 항목으 로 구분 되어 있으며 필수 기재 항목은 반드시 입력 해야 합니다. 회원가입 신청양식의 각 필요 사항은 이용신청자가 개인 또는 사업자인지에 따라 달리 적용됩니다.</li>
    <li>② 회원의 필수 기재 항목: 회원아이디 (ID), 비밀번호(Password), 성명, 생년월일, 휴대전화번호의 항목을 기재해야 합니다.</li>
    <li>③ 마스터회원의 필수 기재 항목: 회원아이디(ID), 이메일 주소, 비밀번호(Password), 이름, 휴대전 화번호를 기재해야 하며, 사업자등록증 사본, 통신판매업신고증 사본, 통장 사본 등 레슨 및  PLAY 개설 및 판매, 정산에 필요한 서류를 제출해야 합니다.</li>
    <li>④ 회사는 회원가입 때 제2항 또는 제3항에 표시된 필수항목 외에 추가적인 정보(예: 유선전화번 호 등)를 요청할 수 있으며, 이용신청자는 그러한 추가적인 정보의 제공을 거부할 수 있습니다. 추가적인 정보는 회원에게 다양하고 유익한 정보를 제공하기 위해서만 사용되며 그 밖의 목적으 로 사용할 수 없습니다.</li>
    <li>⑤ 회사는 모든 회원에게 광고성 이메일(E-mail) 수신여부, 광고성 문자메시지(SMS) 수신여부, 제 휴회사 및 개인정보 위탁관리회사에 대한 개인정보 제공∙이용에 대한 동의 여부를 확인할 수 있 습니다.</li>
</ul>
<p class="sub_title">제9조 (이용신청의 승낙, 거부, 유보)</p>
<ul class="content">
    <li>① 회사는 이 약관 제8조에서 정한 기재항목을 사실대로 정확하게 기재하고 이 약관에 동의한 자 의 가입 신청에 대하여 서비스의 이용을 승낙함을 원칙으로 합니다.</li>
    <li>② 회사는 회원의 가입신청에 대해서는 법령에 따라 이용 할 수 있는 본인확인기관을 통하여 본인 여부를 확인하는 방식의 본인확인절차를 이행하며, 마스터회원에 대해서 제공받은 증빙자료 등으로 본인확인절차를 이행합니다. 회사는 본인확인절차를 이행할 수 없는 이용신청에 대해서는 신청자에게 증빙자료를 요청할 수 있습니다.</li>
    <li>③ 회사는 다음과 같은 사유가 있으면, 이용신청에 대한 승낙을 거부할 수 있습니다.
