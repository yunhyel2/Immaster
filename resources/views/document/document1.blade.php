<p class="sub_title">제21조 (거래완료)</p> 
<ul class="content">
    <li>① 구매확정이 이루어진 이후에 상품의 하자 등 결제대금의 환불사유가 발생하면 회사는 이에 개입하지 않습니다. 구매확정 이후에 발생하는 마스터와 일반회원 간 모든 분쟁은 거래 당사자인 마스터와 일반회원이 해결해야 합니다.</li>
    <li>② 구매확정 기간 내에 일반회원에게서 구매확정, 환불에 대한 의사표시가 없으면 회사는 해당 거래에 대하여 구매확정의 의사가 있는 것으로 간주하여 다음 영업일에 자동구매확정으로 처리 (이하 “자동구매확정”)할 수 있습니다. 다만, 구매확정 기간 내에 일반회원이 실제 상품 또는 용역 을 제공받지 못했다면 일반회원은 미완료 신고를 할 수 있습니다.</li>
    <li>③ 자동구매확정 처리가 완료되면, 회사는 보관 중인 결제대금 중 마스터에게 지급할 상품판매대금을 마스터가 지정한 방법으로 지급할 수 있습니다.</li>
    <li>④ 자동구매확정이 이루어졌다면 일반회원은 미완료, 불만족 등의 사유로 회사에 이의를 제기할 수 없으며, 상품구매대금의 환불 등 모든 문제는 마스터와 직접 해결해야 합니다.</li>
</ul>

<p class="sub_title">제22조 (환불/취소)</p> 
<ul class="content">
    <li>① ’전자상거래 등에서의 소비자보호에 관한 법률’ 제17조에 따라 일반회원은 환불을 요청할 수 있으며, 환불에 관한 일반적인 사항은 ’전자상거래 등에서의 소비자보호에 관한 법률’ 등 관련 법령이 마스터가 제시한 조건보다 우선합니다. 단, 다음 각 호의 경우에는 일반회원이 환불을 요청 할 수 없습니다.
        <ul>
            <li>1. 일반회원의 귀책사유로 말미암아 상품이 훼손된 경우</li>
            <li>2. 일반회원의 사용 또는 일부 소비로 말미암아 상품의 가치가 현저히 감소한 경우</li>
            <li>3. 기타 일반회원이 환불을 요청할 수 없는 합리적인 사유가 있는 경우</li>
        </ul>
    </li>
    <li>② 회사는 일반회원으로부터 환불의 의사표시를 접수하면, 즉시 그러한 사실을 마스터에게 통보 합니다.</li>
    <li>③ 환불에 필요한 비용은 귀책사유가 있는 쪽에서 부담합니다.</li>
    <li>④ 일반회원이 환불을 요청하더라도 마스터가 상품 또는 용역을 다시 제공하는 것으로 일반회원과 합의가 된다면, 이 때는 상품 또는 용역을 다시 제공하는 것으로 처리 가능합니다.</li>
    <li>⑤ 회사가 일반회원의 구매대금 결제 확인에 대해 통지한 후 마스터가 상당 기간 일반회원의 주문 정보를 확인하지 않거나 상품 또는 용역을 제공하지 않아 일반회원이 거래 취소를 요구하면 회사는 해당 거래를 취소할 수 있으며, 거래가 취소되면 보관 중인 상품 구매대금은 일반회원에게 환불됩니다. 아울러, 회사는 개별적인 정책으로 일반회원의 요구 없이도 자동 환불처리 등의 조치를 할 수 있으며 그럴 경우 미리 해당 내용을 일반회원에게 공지합니다.</li>
    <li>⑥ 회사는 마스터가 일반회원의 환불에 대한 사실확인 진행절차에 적극적으로 임하지 않거나 지연하면 일반회원에게서 환불의 원인을 파악한 후 해당 거래를 취소하여 일반회원에게 보관 중인 결제대금을 환불할 수 있습니다.</li>
    <li>⑦ 회사는 일반회원의 환불 요청이 정당하지 않다고 판단하면 일반회원의 의사표시를 취소하여 보관 중인 결제대금을 마스터에게 지급할 수 있습니다. 또한, 일반회원의 환불 등이 접수된 날로 부터 7일이 지날 때까지 전화, 이메일(E-mail) 등으로 연락되지 않으면 환불의 의사를 철회한 것 으로 간주하여 보관 중인 결제대금을 마스터에게 지급할 수 있습니다.</li>
    <li>⑧ 확인된 거래가 취소되어 결제대금을 환불할 경우는 회사는 거래가 취소된 날로부터 2영업일 이내에 일반회원에게 환불에 필요한 조치를 합니다. 신용카드로 결제했을 때는 환불을 요청한 즉시 결제 승인이 취소되고, 환불조치를 합니다.</li>
    <li>⑨ 카드결제로 구매한 건의 환불은 카드결제 취소를 통해서만 가능하며, 어떠한 경우에도 현금잔고로 환불되지 않습니다.</li>
    <li>⑩ 결제완료 상태에서는 취소신청 접수 시 즉시 취소처리가 완료됩니다. 환불의 규정은 아래와 같습니다.
        <ul>
            <li>1. 마스터가 레슨 또는 PLAY의 취소신청을 하는 경우 
                <ul>
                    <li>- 레슨 또는 PLAY 등록 후: 취소 불가를 원칙으로 함 (상해에 따른 취소 제외)</li>
                    <li>- 잦은 취소 시, 마스터에게 페널티 적용이 가능함</li>
                    <li>- 상해에 따른 취소 시, ‘상해’ 선택 후 3주내에 진단서 필수 제출. ‘상해’에 의한 취소 시, 그 판단 이 완료되는 시점(진단서 제출 후 3주)까지 환불이 유예될 수 있으나 ‘상해’ 사실이 증명되면, 참가자에게 취소사유를 안내하고 참가자에게 100% 환불 </li>
                </ul>
            </li>
            <li>2. 일반회원 신청자가 신청을 취소하는 경우
                <ul>
                    <li>- 레슨 3일 전: 100% 환불</li>
                    <li>- 레슨 2일 전: 60% 환불</li>
                    <li>- 레슨 1일 전 ~ 당일: 환불 불가</li>
                    <li>- 잦은 취소 시, 신청자에게 페널티 적용이 가능</li>
                    <li>- 무단 불참 시, 신청이 제한될 수 있음</li>
                    <li>- 상해에 따른 취소 시, ‘상해’ 선택 후 3주내에 진단서 필수 제출. ‘상해’에 의한 취소 시, 그 판단이 완료되는 시점(진단서 제출후 3주)까지 환불이 유예될 수 있으나 ‘상해’ 사실이 증명되면, 마스터에게 취소사유를 안내하고 참가자에게 100% 환불</li>
                </ul>
            </li>
            <li>3. 천재지변의 경우(전란, 정부의 명령 포함)에는 상기의 규정에 무관하게 페널티 없이 환불하여 드립니다.</li>
            <li>4. 참가인원 미달에 의한 취소
                <ul>
                    <li>- 마스터는 레슨 및 PLAY에 대한 공지 시 최소참가신청인원을 기재할 수 있으며, 참가 인원이 미달되는 때에는 레슨 및 PLAY를 취소할 수 있음을 함께 공지하여야 함</li>
                    <li>- 참가 인원 미달 시, 레슨 및 PLAY 모집 종료 후 즉시 참가자에게 안내함 (이메일, SMS, 푸시알림)</li>
                </ul>
            </li>
        </ul>
    </li>
</ul> 
 
<p class="sub_title">제23조 (권한 정지/강제 탈퇴)</p> 
<ul class="content">
    <li>① 회사는 회원에게 부여한 권한을 정지/강제 탈퇴할 수 있으며 그 사유는 아래와 같습니다. 
        <ul>
            <li>1. 본인 책임으로 인해 아임마스터 내에서 이루어지는 활동과 관련하여 문제가 발생하는 경우.</li>
            <li>2. 법적 이슈 등의 분쟁 발생을 고려하여, 강제 탈퇴 처리 후에도 컨텐츠, 레슨, PLAY, 리뷰, 댓글 등의 Data는 바로 삭제하지 않고 최대 6개월 간 보존.<br/>(단, 분쟁 발생 후 미완료 시 6개월을 초과하여도 분쟁 완료 시점까지 보존).</li>
            <li>3. 다음과 같은 사항이 발생하는 경우 권한 정지 및 강제 탈퇴 조치 시행
                <ul>
                    <li>① 타인의 전화번호 혹은 비밀번호를 도용하는 행위</li>
                    <li>② 개인 정보를 허위 또는 타인의 것으로 등록하는 행위</li>
                    <li>③ 공공질서, 미풍양속을 저해하는 저작물을 등록 또는 유통시키는 행위</li>
                    <li>④ 타인이 저작권을 보유한 컨텐츠를 무단으로 게시하는 행위</li>
                    <li>⑤ 광고성/홍보성 저작물을 지속적으로 게시하는 행위</li>
                    <li>⑥ 사이트 내에서 불법적으로 상품을 판매하거나 상행위를 하는 행위</li>
                    <li>⑦ 아임마스터가 제공하는 프로필 및 개인 정보(이름, 연락처 등)을 무단 캡처(저장) 하는 행위 및 이를 유출하여 외부에 게시하는 행위</li>
                    <li>⑧ 다른 고객에게 무단으로 개인 정보를 요구하는 행위</li>
                    <li>⑨ 아임마스터에 대한 허위 사실을 유포하거나 기타의 방법으로 업무를 방해하는 행위</li>
                    <li>⑩ 사전 동의 없이 아임마스터 명의를 무단으로 도용하는 행위</li>
                    <li>⑪ 다른 고객에게 정당한 사유 없이 금전적 손실을 입히는 행위 </li>
                    <li>⑫ 레슨 또는 PLAY 개설 후 사전 고지(Class 시행 예정 시간 48시간 이전까지 사전 고지한 경우만 정당한 사전 고지로 정의함) 없이 시행을 하지 않는 행위</li>
                    <li>⑬ 레슨 또는 PLAY 참가 신청을 한 회원에게 추가 비용이나 일시 변경을 강요하는 행위</li>
                    <li>⑭ 레슨 또는 PLAY에 참석한 회원을 대상으로 비방, 인격모독을 하거나 물리적/정신적 위해를 가하는 행위</li>
                    <li>⑮ 기타 아임마스터 서비스 내 활동을 통해 당사 및 서비스 이용 고객에게 정신적/물리적 피해나 금전적 손실을 발생시키는 모든 행위</li>
                </ul>
            </li>
        </ul>
    </li>
    <li>② 위 사유 발생시, 모든 권한 정지하고 1개월 간의 소명 기간 부여 후 소명 불가 시 탈퇴 처리 됩니다. </li>
</ul>

<p class="sub_title">제24조 (금지행위)</p> 
<ul class="content">
    <li>① “다이렉트신청” 및 “견적문의” 외에 마스터와 일반회원이 직접 거래하는 행위(직거래)는 거래의 안전을 위하여 권하지 않으며, 직거래를 통하여 발생한 제반 문제에 대한 책임은 거래 당사자에게 있으며 회사는 이에 대해 어떠한 책임도 지지 않습니다.</li>
    <li>② “다이렉트신청” 및 “견적문의” 외에 직거래를 하거나 이를 유도한 것으로 확인된 마스터는 서비스 이용이 정지되거나 이용계약이 해지될 수 있으며, 회원은 직거래를 하거나 유도한 마스터를 회사가 운영하는 안전거래센터에 신고할 수 있습니다.</li>
    <li>③ 회사가 제공하는 서비스 이용방법을 따르지 않고 비정상적인 방법으로 서비스를 이용하거나 시스템에 접근하는 행위는 금지됩니다.</li>
    <li>④ 시스템 부정행위가 확인되면 회사는 해당 회원에게 부가적으로 제공한 혜택의 일부 또는 전부 를 회수하거나, 특정 서비스의 이용을 제한하고, 이용계약을 해지하는 등의 조치를 할 수 있습니다.</li>
    <li>⑤ 타인의 명의, 카드정보, 계좌정보 등을 무단으로 사용하여 회사가 제공하는 구매서비스를 이용하는 행위는 금지됩니다.</li>
    <li>⑥ 물품판매 또는 용역제공을 가장하여 자금을 융통하는 것과 같이 여신전문금융업법 등 법령에 따라 금지된 방법으로 비정상적인 결제를 하는 것은 금지되며, 해당 내용이 확인되었을 때 회사는 회원의 거래를 취소하거나 중지시키고 관련기관에 통보할 수 있습니다.</li>
    <li>⑦ 실제 상품의 제공이 없는 등 상품의 실질적인 구매의사가 없는 구매행위와 회사가 제공하는 할인율 등을 이용하여 비정상적인 거래를 하는 것은 금지되며, 해당 내용이 확인되면 회사는 해당 거래를 취소하고 서비스이용을 제한하거나 이용계약을 해지할 수 있습니다.</li>
</ul>
<p class="sub_title">제25조 (안전거래센터)</p> 
<ul class="content">
    <li>① 회사는 타인의 권리 침해, 법령의 위반 등 회원이 서비스를 이용할 때 발생할 수 있는 거래의 안전과 관련된 법적 문제를 조정하기 위하여 안전거래센터를 운영합니다.</li>
    <li>② 안전거래센터는 다음과 같은 역할을 합니다.
        <ul>
            <li>1. 부적합물의 판매에 대한 조치 </li>
            <li>2. 타인의 권리를 침해하는 행위에 대한 조치 </li>
            <li>3. 기타 거래의 안전 또는 법령을 위반하는 행위에 대한 조치</li>
        </ul>
    </li>
    <li>③ 안전거래센터의 조정에 회원은 신의성실의 원칙에 따라 성실히 응해야 합니다.</li>
</ul>

<p class="sub_title">제26조 (회사의 면책)</p> 
<ul class="content">
    <li>① 상품의 매매와 관련하여 발생하는 상품 및 용역의 제공, 청약철회, 환불 등의 거래진행은 거래 당사자인 마스터와 일반회원 간에 수행되어야 하며, 회사는 거래진행에 관여하지 않으며 어떠한 책임도 부담지지 않습니다.</li>
    <li>② 제13조 제2항의 사유로 서비스를 일시적으로 중단하여 이용자 또는 제3자가 손해를 입었을 때 회사는 이에 대해 책임을 지지 않습니다.</li>
    <li>③ 제13조 제3항의 사유로 서비스를 제한하거나 중단하게 된다면 회사는 불가항력을 이유로 그 책임을 지지 않습니다. </li>
    <li>④ 회사는 이용자의 귀책사유로 서비스 이용에 장애가 발생하는 것에 대해 책임을 지지 않습니다.</li>
    <li>⑤ 이용자가 자신의 개인정보를 타인에게 유출하거나 제공하여 발생하는 피해에 대해 회사는 일절 책임을 지지 않습니다.</li>
    <li>⑥ 회사의 서비스 화면에서 링크, 배너 등을 통하여 연결된 회사(이하 피연결회사)와 이용자간에 이루어진 거래에 회사는 개입하지 않으며, 해당 거래에 대하여 책임을 지지 않습니다.</li>
    <li>⑦ 회사는 서비스 화면에 표시되는 마스터 또는 제3자가 제공하는 상품 또는 정보 등의 정확성, 적시성, 타당성 등을 보증하지 않으며, 그와 관련하여 어떠한 책임도 지지 않습니다.</li>
    <li>⑧ 회사는 제29조 제1항에 따른 서비스나 기능의 전부 또는 일부를 수정하거나 중단하는 것에 대해 책임을 지지 않습니다.</li>
</ul>

<p class="sub_title">제3장 기타사항</p> 
<p class="sub_title">제27조 (준거법 및 분쟁조정)</p> 
<ul class="content">
    <li>① 이 약관과 회사와 회원 간의 서비스 이용 계약, 회원 상호 간의 물품거래에는 대한민국 법령이 적용됩니다.</li>
    <li>② 이 서비스와 관련하여 회사와 이용자 사이에 분쟁이 발생하여 소송이 제기되었을 때에는 최초 30일간 상호 간의 합의를 위한 노력을 기하고, 중재 실패 시 대한상사중재원에 중재를 신청합니다.</li>
</ul>
<p class="sub_title">제28조 (기타 조항)</p> 
<ul class="content">
    <li>① 회사는 필요하면 특정 서비스나 기능의 전부 또는 일부를 사이트를 통해 미리 공지한 후 일시적 또는 영구적으로 수정하거나 중단할 수 있습니다.</li>
    <li>② 각 당사자는 상대방의 서면동의 없이 이 약관상의 권리와 의무를 제3자에게 양도하거나 처분할 수 없습니다.</li>
    <li>③ 이 약관과 관련하여 당사자 간의 합의에 따라 추가로 작성된 계약서, 협정서, 통보서 등과 회사의 정책변경, 법령의 제·개정 또는 공공기관의 고시나 지침 등에 따라 회사가 사이트를 통해 공지하는 내용도 이용계약의 일부를 구성합니다.</li>
    <li>④ 회사는 회원이 서비스를 이용할 때 발생할 수 있는 정당한 의견이나 불만 사항을 적극적으로 수렴하여 해결하고, 회원 상호 간의 분쟁을 조정하기 위하여 고객센터를 설치하고 운영합니다. 회사는 회원이 제기하는 각종 불만사항과 의견을 적극적으로 검토한 후 정당하다고 판단될 때에는 이를 신속하게 처리하며, 즉시 처리하기가 어려운 사항이 있으면 그 사유와 처리기간을 이메일(Email) 혹은 전화로 회원에게 통보 합니다.</li>
</ul>
 
