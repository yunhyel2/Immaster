<p class="sub_title">제1조 (목적)</p>
<p>이 약관은 투어텔레콤(이하 “회사”)가 운영하는 사이트(www.immaster.kr) 및 아임마스터 모바일 어플리케이션 등(이하 “사이트”)에서 마스터가 일반회원에게 재능을 공유하는 활동(이하 “레슨 및 플레이”)을 개설/운영하는 것에 대해 관련 당사자 간의 권리, 의무를 확정하고 이를 이행하여 상호 발전을 도모하는 것을 목적으로 합니다.</p> 

<p class="sub_title">제2조 (용어의 정의)</p>
<p>이 약관에서 사용되는 용어의 정의는 이 약관에서 별도로 규정하는 경우를 제외하고 “아임마스터 이용약관”(이하 “이용약관”) 제2조를 따릅니다.</p>
<p class="content">① 서비스 이용료: “마스터회원”이 “회사”가 제공하는 “서비스”를 이용하는 대가로 “회사”에게 지급하는 금액을 말합니다.</p>

<p class="sub_title">제3조 (효력)</p>
<ul class="content">
    <li>① 회사는 이 약관에서 규정되지 않은 세부적인 내용을 아임마스터 이용정책(이하 "이용정책")에 규정할 수 있으며, 이를 마스터 개별 전자메일을 통하여 공지하며, 이용정책은 이 약관과 더불어 아임마스터 서비스 이용계약(이하 “이용계약”)의 일부를 구성합니다.</li>
    <li>② 회사는 관련 법령의 규정 등에 따라 레슨 및 플레이 서비스 중 특정 서비스에 관한 약관(이하 "개별약관")을 별도로 제정할 수 있으며, 마스터가 개별약관에 동의하면 개별약관은 이용계약의 일부를 구성하고, 개별약관에 이 약관과 상충하는 내용이 있으면 개별약관이 우선하여 적용됩니다.</li>
    <li>③ 일반회원 이용약관의 내용 중 성질상 레슨 및 플레이 서비스에 적용될 수 있는 내용은 제2항과 동일하게 적용됩니다. 이 약관에 따라 레슨 및 플레이를 개설하고자 하는 자는 일반회원 이용약관의 내용을 숙지하고 본 항의 내용에 동의하는 것임을 확인합니다.</li>
    <li>④ 이 약관, 이용정책, 개별약관의 변경이 있으면 회사는 변경 내용의 효력발생일 7일 이전(다만, 마스터에게 불리한 내용으로 변경할 때에는 30일 이전)에 해당 변경 사항을 마스터에게 전자메일 등을 통해 공지하며, 마스터가 변경된 약관, 이용정책, 개별약관에 동의하지 않으면 마스터 탈퇴(서비스계약의 해지)를 요청할 수 있으며, 변경 적용일까지 거부 의사를 표시하지 않으면 변경 약관, 이용정책, 개별약관에 동의한 것으로 간주합니다.</li>
    <li>⑤ 변경된 약관은 그 적용일자 이전으로 소급하여 적용되지 않습니다.</li>
    <li>⑥ 서비스 이용 기준일 현재 일반회원 이용약관상의 만 19세 미만의 회원이 레슨 및 플레이를 개설하고자 한다면 회사는 해당 회원에게 법정대리인의 동의서와 입증 자료의 제출을 요구할 수 있으며, 해당 회원은 이에 동의해야 합니다. 해당 회원이 본 규정을 지키지 않으면 해당 회원은 레슨 및 플레이를 개설할 수 없습니다.</li>
    <li>⑦ 일반회원 이용약관상의 회원이 레슨 및 플레이를 개설하고자 한다면 회사는 해당 회원에게 신원확인 목적 등을 위하여 범용공인인증 또는 회사가 인정하는 신원확인 수단에 대한 자료를 요구할 수 있으며, 해당 회원은 범용공인인증서 등 회사가 요구하는 관련 자료를 회사가 요구하는 기한 내에 제출해야 합니다. 해당 회원이 본 규정을 지키지 않으면 회사는 해당 회원에 대하여 서비스 사이트 로그인, 레슨 및 플레이의 개설 등 회사가 제공하는 서비스의 이용을 제한할 수 있습니다.</li>
</ul>
      
<p class="sub_title">제4조 (서비스 이용료)</p>
<ul class="content">
    <li>① 서비스 이용료는 서비스를 이용하는 데 따른 대가로 마스터가 회사에 지급해야 하는 금액을 의미합니다. 회사는 일반회원으로부터 예치 받은 금액에서 서비스 이용료를 공제하고 마스터에게 정산합니다.</li>
    <li>② 서비스 이용료는 판매가(마스터가 정한 상품가격)에 회사가 정한 비율 10% (이하 “서비스 이용료율”)를 곱한 금액으로 합니다. 서비스 이용료 = 판매가 × 0.1</li>
    <li>③ 서비스 이용료와 서비스 이용료율 등은 회사와 마스터 간의 협의 또는 회사의 내부 사정에 따라 변경될 수 있습니다.</li>
    <li>④ 마스터는 서비스 이용료 및 서비스 이용료율, 이용방법 등 관련 정책을 전자메일 등을 통해 확인할 수 있습니다.</li>
    <li>⑤ 마스터 최초 등록 시 회사가 안내하는 얼리버드 혜택에 따라 등록한 마스터의 경우, 서비스 시범운영기간(이하 “시범운영기간”) 동안 서비스 이용료가 면제됩니다.</li>
    <li>⑥ 시범운영은 원활한 서비스 제공을 위한 시범 서비스 기간으로 회사가 임의로 지정합니다. 회사가 시범운영을 종료할 때는 서비스 사이트에 공지하고 이메일, 문자메시지 등으로 이용자에게 개별 통지합니다(이용자의 연락처 미기재, 변경 등으로 개별 통지가 어려울 때에는 본 약관에 따라 공지를 하는 것을 개별 통지한 것으로 간주합니다).</li>
</ul>

<p class="sub_title">제5조 (마스터의 관리의무)</p>
<ul class="content">
    <li>① 서비스 사이트에서 레슨 및 플레이의 개설은 회사의 승인을 거쳐 마스터 등록이 완료되는 동시에 가능하며, 이를 위해서 마스터는 레슨 및 플레이에 관한 정보를 웹페이지를 통하여 직접 등록해야 합니다.</li>
    <li>② 레슨 및 플레이의 가격은 표준마진율, 서비스 이용료 등을 고려하여 마스터 스스로 결정합니다.</li>
    <li>③ 마스터는 예약현황 등 수시로 바뀌는 사항에 대한 데이터를 적절히 관리해야 하며, 마스터는 데이터를 허위로 기재할 수 없습니다.</li>
    <li>④ 마스터는 전자상거래 등에서의 소비자보호에 관한 법률(이하 “전상법”), 전자금융거래법, 여신전문금융업법, 공중위생관리법, 전기통신사업법, 식품위생법, 전기용품안전관리법, 부가가치세법, 정보통신망 이용촉진 및 정보보호 등에 관한 법률, 상표법, 저작권법 등 서비스 사이트에서 레슨 및 플레이 개설과 관련하여 법령이 요구하는 사항을 지켜야 합니다.</li>
    <li>⑤ 마스터는 회사가 서면으로 미리 승인하지 않으면 서비스 사이트의 상호나 로고 등을 사용할 수 없습니다.</li>
    <li>⑥ 마스터는 서비스 사이트를 통하지 않고 일반회원에게 직접 레슨 및 플레이를 판매/제공하거나 이를 유도해서는 안 되며, 이를 위반하면 회사는 해당 마스터의 서비스 이용을 정지하거나 서비스이용 계약을 해지할 수 있습니다.</li>
    <li>⑦ 마스터는 레슨 및 플레이를 신청한 일반회원에게 사이트에서 공지한 것과 동일한 내용(일정, 인원 준수 등)으로 레슨 및 플레이 서비스를 제공하여야 하고, 합리적 이유 없이 일방적으로 레슨 및 플레이 신청을 거부하거나 취소하여서는 아니 되며, 이에 대한 보증 서비스를 자신의 책임과 비용으로 시행하여야 합니다.</li>
    <li>⑧ 마스터는 세금계산서 발급 등 일반회원의 요구에 응해야 합니다.</li>
    <li>⑨ 마스터는 일반회원의 문의에 성실하고 정확하게 대답해야 합니다. 마스터의 불성실, 부정확한 답변으로 일반회원에게 손해가 발생하면 마스터가 이에 대해 책임을 부담합니다.</li>
    <li>⑩ 마스터는 레슨 및 플레이 등록과 관련하여 특정한 인허가 자격이 요구되면 이에 대한 요건을 만족한 후 레슨 및 플레이를 광고 또는 등록해야 합니다. 인허가 자격을 갖추지 않은 상태에서 관련 레슨 및 플레이를 제공하여 발생한 모든 민형사상의 책임은 마스터 본인에게 있습니다.</li>
    <li>⑪ 마스터는 레슨 및 플레이 등록 후 장기간 제공 또는 판매되지 않는 레슨 및 플레이를 유지할 수 없으며, 이를 위반하면 회사는 별도 통지 없이 레슨 및 플레이 검색의 효율성 제고와 일반회원 편의 개선을 위해 해당 레슨 및 플레이를 삭제 처리할 수 있습니다.</li>
    <li>⑫ 회사는 마스터의 사유로 상당한 시간이 경과되는 시점까지 레슨 및 플레이의 제공이 되지 않거나 취소율이 일정 수준을 초과하는 때에는 관련하여 마스터 레벨에 대한 감점, 아이디(ID) 정지 등 서비스 품질향상을 위하여 해당 마스터에게 필요한 조치를 할 수 있습니다. 이때 회사는 구체적인 내용에 대한 정책을 합리적인 기준에 따라 정하고 미리 공지합니다. </li>
</ul>
   
<p class="sub_title">제6조 (지식 재산권)</p>
<ul class="content">
    <li>① 마스터는 레슨 및 플레이의 등록 및 제공 등과 관련하여 제3자의 상표권, 특허권, 저작권, 성명권, 초상권 등 제반 지식 재산권을 침해하지 않아야 하며, 제3자의 지식 재산권을 사용할 때에는 정당한 권리자로부터 사용 허락을 받은 후에 사용해야 합니다.</li>
    <li>② 마스터가 등록∙사용한 제반 정보(초상, 성명 포함)에 대하여 제3자가 지식 재산권과 관련한 권리침해를 주장하면 회사는 마스터가 제3자의 권리침해가 아님을 입증(법원의 판결 또는 검찰의 불기소처분 등)할 때까지 해당 마스터 및 플레이에 관한 정보의 등록 및 제공을 중지할 수 있습니다.</li>
    <li>③ 마스터가 등록한 레슨 및 플레이에 관한 정보는 서비스 활성화를 위하여 회사가 제휴한 제3자(및 서비스 사이트)와 다른 회원의 블로그, SNS 등에 노출될 수 있습니다. 다만, 다른 회원이 블로그 또는 SNS 등에 이를 노출하려면 회사가 정한 방침에 동의하고 회사가 허용한 방식으로만 하여야 합니다.</li>
    <li>④ 마스터는 서비스 사이트에 등록∙사용한 정보와 관련하여 제3자로부터 소송 또는 소송 이외의 방법 등으로 이의제기를 받게 되면 회사(및 서비스 사이트)를 면책시켜야 하며, 면책에 실패한 경우 그로 인해 회사(및 서비스 사이트)가 입은 모든 손해를 배상하여야 합니다. </li>
</ul>
  
<p class="sub_title">제7조 (서비스 제공 완료 등록)</p>
<ul class="content">
    <li>① 일반회원의 주문에 따른 결제가 완료된 순간 회사는 마스터에게 주문 정보를 이메일(Email), 팩스, 전화 등의 방법으로 전달하고, 마스터는 해당 주문 정보에 따라 일반회원에게 레슨 및 플레이를 제공하여야 합니다.</li>
    <li>② 전상법 제15조 1항에 따라 마스터는 일반회원의 예약일에 레슨 및 플레이의 제공을 위하여 필요한 조치를 하여야 합니다.</li>
    <li>③ 마스터가 전항의 예약일에 레슨 및 플레이를 제공하지 않거나, 일반회원이 레슨 및 플레이를 정상적으로 제공받지 못하면 마스터는 그에 관한 책임을 부담하여야 합니다.</li>
    <li>④ 회사가 일반회원의 일반회원대금 결제 확인을 통지한 후 마스터가 상당 기간 예약확인을 하지 않거나, 예약확인을 한 이후라도 후속 절차를 이행하지 않으면 회사는 별도의 개별 정책으로 일반회원에게 자동으로 환불 처리하는 등의 조치를 할 수 있습니다. 다만, 일반회원이 희망하지 않는 경우는 제외하며 개별 정책은 미리 공지합니다. </li>
</ul>

<p class="sub_title">제8조 (취소 및 환불)</p>
<ul class="content">
    <li>① 일반회원이 환불을 요청하면 마스터는 전상법 등 관련 법률 및 일반회원 일반약관에 따라 환불을 해주어야 하며, 추가로 발생하는 비용은 환불의 책임이 있는 측에서 부담합니다.</li>
    <li>② 예약확인 후 취소 요청 건에 대해 2영업일 이내에 환불처리나 취소확인 처리를 하지 않으면, 3영업일에 주문 건은 자동으로 환불 처리됩니다.</li>
