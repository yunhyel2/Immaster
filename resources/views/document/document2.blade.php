<p class="sub_title">제15조 (개설 부적합 레슨 및 플레이)</p>
<ul class="content">
    <li>① 다음 각 호의 개설 부적합 레슨 및 플레이 및 매매 부적합 상품 또는 용역이 관련된 레슨 및 플레이는 등록을 금지하며, 개설 부적합 레슨 및 플레이를 등록 했을 때의 모든 책임은 해당 개설 부적합 레슨 및 플레이를 등록한 마스터가 부담합니다.
        <ul>
            <li>1. 허위 또는 과장 광고한 상품 또는 용역, 이와 관련된 레슨 및 플레이</li>
            <li>2. 지식 재산권, 상표권 등 타인의 권리를 침해하는 상품 또는 용역, 이와 관련된 레슨 및 플레이</li>
            <li>3. 형법, 정보통신망이용촉진 및 정보보호 등에 관한 법률 등 관련 법령에서 유통을 금지하는 음란물 관련 레슨 및 플레이</li>
            <li>4. 철도승차권 등 발행자가 통신제휴를 금지한 유가증권 관련 레슨 및 플레이</li>
            <li>5. 유통검정/검사를 통과하지 못한 공산품 관련 레슨 및 플레이</li>
            <li>6. 단말기 보조금 관련 사항이 기재된 상품 또는 용역, 이와 관련된 레슨 및 플레이</li>
            <li>7. 심의되지 않거나 불법 복제된 영상물, 음반, 게임물 등, 이와 관련된 레슨 및 플레이</li>
            <li>8. 장물이나 습득한 유실물, 이와 관련된 레슨 및 플레이</li>
            <li>9. 주류, 담배, 도수가 있는 안경•선글라스, 콘택트렌즈, 군수품, 의약품 등 관련 법령에 따라 통신제휴가 금지되는 상품 또는 용역, 이와 관련된 레슨 및 플레이</li>
            <li>10. 동물보호법에서 정하고 있는 살아 있는 동물들(포유류, 조류, 파충류, 양서류, 어류), 이와 관련된 레슨 및 플레이</li>
            <li>11. 기타 관계 법령에 저촉되거나 회사가 합리적인 사유로 개설을 금지하는 상품 또는 용역, 이와 관련된 레슨 및 플레이</li>
        </ul>
    </li>
    <li>② 회사는 개설 부적합 레슨 및 플레이가 발견되면 해당 레슨 및 플레이의 광고를 삭제하거나 그 판매를 중지시킬 수 있으며, 해당 레슨 및 플레이가 이미 판매되었다면 그 거래를 취소할 수 있습니다. 이때 마스터가 취소된 거래와 관련하여 지급한 서비스 이용료는 환불되지 않습니다.</li>
    <li>③ 회사는 매매부적합상품 또는 용역을 등록한 마스터의 회원 자격을 정지시키거나 탈퇴시킬 수 있으며, 매매부적합상품 또는 용역으로 입은 손해를 해당 마스터에게 청구할 수 있습니다.</li>
    <li>④ 마스터가 제1항의 개설 부적합 레슨 및 플레이 중 위조된 레슨 및 플레이를 개설하여 일반회원에게 손해가 발생했을 때 회사는 손해에 상당하는 금액(일반회원대금 및 일반회원의 정신적 피해에 대한 보상)을 일반회원에게 직접 지급할 수 있습니다. 이때 회사는 일반회원에게 지급한 일반회원대금, 정신적 피해에 대한 보상금(금전적 가치로 환산할 수 있는 수단을 포함함) 및 위 절차와 관련하여 회사가 지출한 제반 경비(상품 또는 용역 운송비 등)를 해당 레슨 및 플레이를 개설한 마스터에게 청구할 수 있습니다.</li>
    <li>⑤ 제4항은 제14조의 회사의 면책을 부인하는 것으로 해석될 수 없습니다.</li>
</ul>

<p class="sub_title">제16조 (금지행위)</p>
<ul class="content">
    <li>① 마스터는 회사 또는 서비스 사이트의 경영 또는 영업 활동을 방해하는 불공정행위를 직∙간접적으로 하거나 제3자로 하여금 하게 해서는 안 됩니다. 특히 마스터는 직접 또는 회사(또는 서비스 사이트)와 경쟁관계에 있는 제3자의 회사(이하 "경쟁회사") 또는 해당 서비스 사이트와 연계∙공조하여 ‘독점규제 및 공정거래에 관한 법률’상 금지되는 불공정행위(예컨대, 사업활동방해 등)등을 하여 회사나 서비스 사이트의 영업에 피해를 주는 행위를 해서는 안 됩니다.</li>
    <li>② 회사는 카테고리 오등록 등 회사와 다른 마스터의 정상적인 영업활동을 방해하는 레슨 및 플레이 개설 및 운영 방식 행위를 금지하고 있으며 마스터에게 해당 레슨 및 플레이의 수정을 요구할 수 있고, 적발횟수에 따라 해당 레슨 및 플레이를 제휴금지 또는 해당 마스터에게 서비스 사이트 이용을 제한할 수 있습니다.
        <ul>
            <li>1. 카테고리 오등록이란 레슨 및 플레이와 관계없는 카테고리에 레슨 및 플레이를 등록하는 것을 말합니다.</li>
            <li>2. 중복등록이란 같은 카테고리와 인접 카테고리에 같은 레슨 및 플레이(같은 레슨 및 플레이로 간주되는 경우를 포함)을 같거나 유사한 제휴조건으로 복수 등록하는 것을 말합니다. 단, 레슨 및 플레이가 2회 이상의 정규 과정인 경우, 동일한 콘텐츠의 레슨 및 플레이라 하더라도 최대 3개까지 서로 다른 일정으로 레슨 및 플레이의 등록이 가능합니다.  - 회사는 구체적인 중복등록을 판단 기준에 대한 정책을 정하여 미리 공지하며, 효율적인 레슨 및 플레이 검색을 위하여 중복등록으로 확인(추정)된 레슨 및 플레이의 검색을 제한하거나 해당 레슨 및 플레이를 삭제하는 등의 조치를 할 수 있습니다.</li>
            <li>3. 스팸성 키워드란 검색노출을 확장하려고 개설하는 레슨 및 플레이와 관련 없는 브랜드명, 인기 키워드를 레슨 및 플레이 등록 내용에 기재하는 것을 말합니다.</li>
            <li>4. 회사는 기타 비정상적인 방법으로 레슨 및 플레이를 노출하는 모든 행위를 금지합니다.</li>
        </ul>
    </li>
    <li>③ 마스터가 회사와 사전 협의 없이 콘텐츠 및 레슨 및 플레이를 서비스 사이트에 올린 것이 확인되는 경우, 회사는 별도의 고지 없이 이를 삭제 또는 편집할 수 있으며, 이용자의 민원 기타 콘텐츠 및 레슨 및 플레이에 대한 이슈가 발생하는 경우 그 사실을 마스터에게 고지하고 삭제 또는 편집할 수 있습니다.</li>
    <li>④ 마스터가 제1항, 제2항, 제 3항 및 이 약관에서 금지하는 행위를 했을 때 회사는 마스터의 서비스 이용정지, 계약해지 등 회사의 손해를 최소화할 수 있는 조치를 할 수 있고, 마스터는 이에 대하여 이의를 제기하지 않습니다. </li>
</ul>

<p class="sub_title">제17조 (불공정 행위 금지)</p>
<ul class="content">
    <p>회사는 다음 각 호의 어느 하나에 해당하는 불공정 거래 행위 또는 부당한 공동행위를 하지 않으며, 독점규제 및 공정거래에 관한 법률 등 관련 법령을 지킵니다.</p>
    <li>1. 정상적인 거래 관행에 비추어 부당한 조건 등 불이익을 마스터에게 제시하여 거래하도록 강제하는 행위</li>
    <li>2. 서비스 이용료 및 멤버십 포인트의 결정과 관련하여 회사와 경쟁관계에 있는 제3의 회사와 담합하는 등의 불공정 행위</li>
    <li>3. 마스터들을 부당하게 차별적으로 취급하여 서비스 이용료를 마스터별로 다르게 정하는 행위. 다만, 합리적인 사유가 있으면 다르게 정할 수 있습니다.</li>
    <li>4. 회사와 경쟁관계에 있는 제3의 회사와 거래하는 것을 금지하거나 부당하게 강요하는 행위 등 불이익을 주는 행위</li>
</ul>

<p class="sub_title">제18조 (비밀유지)</p>
<ul class="content">
    <li>① 각 당사자는 법령상 요구되는 경우를 제외하고는 상대방으로부터 취득한 일반회원정보, 기술정보, 생산 및 제휴계획, 노하우 등 비밀로 관리되는 정보를 제3자에게 누설해서는 안 되며, 그 정보를 이용계약의 이행 이외의 목적으로 사용해서는 안 됩니다.</li>
    <li>② 제1항의 의무는 이용계약의 종료 후에도 3년간 존속합니다.</li>
</ul>

<p class="sub_title">제19조 (관할법원)</p>
<p class="content">이용계약과 관련하여 회사와 마스터 간에 분쟁이 발생하여 소송이 제기될 때에는 최초 30일간 상호 간의 합의를 위한 노력을 기하고, 중재 실패 시 대한상사중재원에 중재를 신청합니다.</p>

<p class="sub_title">제20조 (보칙)</p>
<ul class="content">
    <li>① 회사가 전자금융거래법 제2조 19호에 따른 전자지급결제대행의 업무를 하는 자에게 정산대행을 위탁하는 경우 이 약관에서 정산과 관련하여 회사로 기재된 부분은 전자지급결제대행업자가 관련 업무를 처리하는 것으로 합니다.</li>
    <li>② 마스터는 주소지 또는 대금결제를 위한 통장계좌 등에 변경이 있으면 즉시 회사에 해당 내용을 통지해야 하며, 회사는 통지의 지연으로 발생한 손해에 대해 책임을 지지 않습니다.</li>
    <li>③ 각 당사자는 상대방의 서면 동의 없이 이용계약상의 권리와 의무를 제3자에게 양도하거나 처분할 수 없습니다.</li>
    <li>④ 이 약관과 관련하여 당사자가 서로 합의하여 추가로 작성한 계약서, 협정서, 약정서 등과 회사의 정책변경, 법령의 제•개정 또는 공공기관의 고시•지침 등에 따라 회사가 서비스 사이트를 통해 마스터에게 공지하는 내용도 이용계약의 일부를 구성합니다.</li>
    <li>⑤ 이 약관과 이용정책, 개별약관에서 정하지 않은 사항은 일반회원 이용약관을 따르며, 일반회원이용약관에서 정하지 않은 사항은 관련 법령과 상관례를 따릅니다.</li>
</ul>
<p class="sub_title">■ 부칙<br/>제 1 조 (시행일) 이 약관은 2017년 3월 20일부터 시행합니다.</p>
  